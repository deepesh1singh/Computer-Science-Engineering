// Convert hex color to RGB
function hexToRgb(hex) {
    const result = /^#?([a-f\d]{2})([a-f\d]{2})([a-f\d]{2})$/i.exec(hex);
    return result ? 
        `${parseInt(result[1], 16)}, ${parseInt(result[2], 16)}, ${parseInt(result[3], 16)}` 
        : '108, 99, 255';
}

// Book actions
function previewBook(title, filePath) {
    if (!filePath) {
        showNotification(`Preview not available for: ${title}`, 'warning');
        return;
    }
    
    try {
        // Check if file exists
        checkFileExists(filePath)
            .then(exists => {
                if (exists) {
                    // Open PDF in new tab for preview
                    window.open(filePath, '_blank', 'noopener,noreferrer');
                    showNotification(`Opening preview: ${title}`, 'success');
                } else {
                    showNotification(`File not found: ${title}`, 'warning');
                    // Fallback: Show message about file structure
                    console.log(`File path: ${filePath}`);
                    console.log(`Expected file structure: files/COURSE_NAME/BOOK_FILE.pdf`);
                }
            })
            .catch(error => {
                console.error('Error checking file:', error);
                showNotification(`Error accessing file: ${title}`, 'error');
            });
    } catch (error) {
        console.error('Preview error:', error);
        showNotification(`Cannot preview: ${title}`, 'error');
    }
}

function downloadBook(title, filePath) {
    if (!filePath) {
        showNotification(`Download not available for: ${title}`, 'warning');
        return;
    }
    
    try {
        // Create a temporary link for download
        const link = document.createElement('a');
        link.href = filePath;
        
        // Extract filename from path
        const fileName = filePath.split('/').pop() || `${title.replace(/\s+/g, '_')}.pdf`;
        link.download = fileName;
        
        // For security and proper download handling
        link.target = '_blank';
        link.rel = 'noopener noreferrer';
        
        // Append to document, click, and remove
        document.body.appendChild(link);
        link.click();
        document.body.removeChild(link);
        
        showNotification(`Downloading: ${title}`, 'success');
        
        // Optional: Log download attempt
        console.log(`Download attempted: ${title} from ${filePath}`);
        
    } catch (error) {
        console.error('Download error:', error);
        showNotification(`Cannot download: ${title}`, 'error');
        
        // Fallback: Try opening in new tab
        try {
            window.open(filePath, '_blank', 'noopener,noreferrer');
            showNotification(`Opening file in browser: ${title}`, 'info');
        } catch (fallbackError) {
            console.error('Fallback also failed:', fallbackError);
        }
    }
}

// Helper function to check if file exists
async function checkFileExists(url) {
    try {
        const response = await fetch(url, { method: 'HEAD' });
        return response.ok;
    } catch (error) {
        console.error('File check error:', error);
        return false;
    }
}

function shareBook(title) {
    const shareData = {
        title: title,
        text: `Check out this book from Tech Book Library: ${title}`,
        url: window.location.href
    };
    
    if (navigator.share && navigator.canShare(shareData)) {
        navigator.share(shareData)
            .then(() => console.log('Shared successfully'))
            .catch(error => console.log('Error sharing:', error));
    } else {
        // Fallback for browsers without Web Share API
        const textToCopy = `Check out "${title}" from Tech Book Library at: ${window.location.href}`;
        navigator.clipboard.writeText(textToCopy)
            .then(() => {
                showNotification('Link copied to clipboard!', 'success');
            })
            .catch(err => {
                // Last resort fallback
                prompt('Copy this link to share:', window.location.href);
            });
    }
}

